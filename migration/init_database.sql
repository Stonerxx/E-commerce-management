-- 7. 商品主表
CREATE TABLE product (
    id              NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id     NUMBER(11)      NOT NULL,
    name            VARCHAR2(200)   NOT NULL,
    description     CLOB,
    main_image      VARCHAR2(500)   NOT NULL,
    status          NUMBER(1)       DEFAULT 1 NOT NULL,
    price_min       NUMBER(10,2)    DEFAULT 0 NOT NULL,
    sales_count     NUMBER(11)      DEFAULT 0 NOT NULL,
    view_count      NUMBER(11)      DEFAULT 0 NOT NULL,
    avg_rating      NUMBER(3,2)     DEFAULT 0 NOT NULL,
    created_at      DATE            DEFAULT SYSDATE NOT NULL,
    updated_at      DATE            DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_product_category FOREIGN KEY (category_id) REFERENCES category(id)
);
CREATE INDEX idx_product_category ON product(category_id);
COMMENT ON TABLE product IS '商品主表';
COMMENT ON COLUMN product.id IS '商品ID';
COMMENT ON COLUMN product.category_id IS '分类ID';
COMMENT ON COLUMN product.name IS '商品名称';
COMMENT ON COLUMN product.description IS '商品描述';
COMMENT ON COLUMN product.main_image IS '主图URL';
COMMENT ON COLUMN product.status IS '上架状态 0=下架 1=上架 2=预售';
COMMENT ON COLUMN product.price_min IS '最低售价（冗余）';
COMMENT ON COLUMN product.sales_count IS '累计销量';
COMMENT ON COLUMN product.view_count IS '浏览量';
COMMENT ON COLUMN product.avg_rating IS '评分均值';
COMMENT ON COLUMN product.created_at IS '创建时间';
COMMENT ON COLUMN product.updated_at IS '更新时间';

-- 触发器：自动更新 product.updated_at
CREATE OR REPLACE TRIGGER trg_product_updated
    BEFORE UPDATE ON product
    FOR EACH ROW
BEGIN
    :NEW.updated_at := SYSDATE;
END;
/

