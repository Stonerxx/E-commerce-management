COMMENT ON TABLE category IS '商品分类表';
COMMENT ON COLUMN category.id IS '分类ID';
COMMENT ON COLUMN category.parent_id IS '父分类ID（根节点为NULL）';
COMMENT ON COLUMN category.name IS '分类名称';
COMMENT ON COLUMN category.level IS '层级 1=一级 2=二级...';
COMMENT ON COLUMN category.sort_order IS '排序序号（小靠前）';
COMMENT ON COLUMN category.status IS '状态 0=禁用 1=启用';
COMMENT ON COLUMN category.icon_url IS '分类图标URL';

-- 5. 优惠券模板表
