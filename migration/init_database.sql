COMMENT ON COLUMN role.id IS '角色ID';
COMMENT ON COLUMN role.role_name IS '角色名称';
COMMENT ON COLUMN role.description IS '角色描述';
COMMENT ON COLUMN role.created_at IS '创建时间';

-- 3. 权限表
