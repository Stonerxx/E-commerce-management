CREATE INDEX idx_inventory_log_sku ON inventory_log(sku_id);
CREATE INDEX idx_inventory_log_operator ON inventory_log(operator_id);
COMMENT ON TABLE inventory_log IS '库存变动日志表';
COMMENT ON COLUMN inventory_log.id IS '日志ID';
COMMENT ON COLUMN inventory_log.sku_id IS 'SKU ID';
COMMENT ON COLUMN inventory_log.change_type IS '变动类型 SALE=销售扣减 CANCEL=取消回滚 RESTOCK=补货入库 ADJUST=人工调整';
COMMENT ON COLUMN inventory_log.change_qty IS '变动数量（正入库 负出库）';
COMMENT ON COLUMN inventory_log.before_stock IS '变动前库存';
COMMENT ON COLUMN inventory_log.after_stock IS '变动后库存';
COMMENT ON COLUMN inventory_log.operator_id IS '操作人ID';
COMMENT ON COLUMN inventory_log.ref_order_id IS '关联订单ID';
COMMENT ON COLUMN inventory_log.remark IS '备注';
COMMENT ON COLUMN inventory_log.created_at IS '操作时间';

