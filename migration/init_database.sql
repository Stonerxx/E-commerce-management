COMMENT ON COLUMN sku.price IS '售价';
COMMENT ON COLUMN sku.original_price IS '原价（划线价）';
COMMENT ON COLUMN sku.stock IS '实际库存';
COMMENT ON COLUMN sku.locked_stock IS '锁定库存';
COMMENT ON COLUMN sku.warning_stock IS '预警库存阈值';
COMMENT ON COLUMN sku.sku_image IS 'SKU图片URL';
COMMENT ON COLUMN sku.status IS '状态 0=停售 1=在售';

-- 11. 库存变动日志表
CREATE TABLE inventory_log (
    id            NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sku_id        NUMBER(20)      NOT NULL,
    change_type   VARCHAR2(30)    NOT NULL,
    change_qty    NUMBER(11)      NOT NULL,
    before_stock  NUMBER(11)      NOT NULL,
    after_stock   NUMBER(11)      NOT NULL,
    operator_id   NUMBER(20),
    ref_order_id  NUMBER(20),
    remark        VARCHAR2(500),
    created_at    DATE            DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_inventory_log_sku FOREIGN KEY (sku_id) REFERENCES sku(id),
