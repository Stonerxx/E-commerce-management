-- 16. 订单主表
CREATE TABLE order_main (
    id                NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_no          VARCHAR2(32)    NOT NULL,
    user_id           NUMBER(20)      NOT NULL,
    address_id        NUMBER(20)      NOT NULL,
    user_coupon_id    NUMBER(20),
    status            NUMBER(1)       DEFAULT 0 NOT NULL,
    total_amount      NUMBER(10,2)    NOT NULL,
    discount_amount   NUMBER(10,2)    DEFAULT 0 NOT NULL,
    pay_amount        NUMBER(10,2)    NOT NULL,
    pay_expire_time   DATE            NOT NULL,
    receiver_snapshot VARCHAR2(500)   NOT NULL,
    remark            VARCHAR2(500),
    created_at        DATE            DEFAULT SYSDATE NOT NULL,
    updated_at        DATE            DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_order_user FOREIGN KEY (user_id) REFERENCES "user"(id),
    CONSTRAINT fk_order_address FOREIGN KEY (address_id) REFERENCES address(id),
    CONSTRAINT fk_order_coupon FOREIGN KEY (user_coupon_id) REFERENCES user_coupon(id) ON DELETE SET NULL,
    CONSTRAINT uk_order_no UNIQUE (order_no)
);
CREATE INDEX idx_order_user ON order_main(user_id);
CREATE INDEX idx_order_address ON order_main(address_id);
CREATE INDEX idx_order_coupon ON order_main(user_coupon_id);
CREATE INDEX idx_order_status_created ON order_main(status, created_at);
COMMENT ON TABLE order_main IS '订单主表';
COMMENT ON COLUMN order_main.id IS '订单ID';
COMMENT ON COLUMN order_main.order_no IS '订单编号';
COMMENT ON COLUMN order_main.user_id IS '用户ID';
COMMENT ON COLUMN order_main.address_id IS '收货地址ID';
COMMENT ON COLUMN order_main.user_coupon_id IS '优惠券领取ID';
COMMENT ON COLUMN order_main.status IS '订单状态 0=待支付 1=已支付 2=已发货 3=已完成 4=已取消';
