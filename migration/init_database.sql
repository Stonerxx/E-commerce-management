-- 4. 商品分类表（自引用）
CREATE TABLE category (
    id          NUMBER(11)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id   NUMBER(11),
    name        VARCHAR2(100)   NOT NULL,
    level       NUMBER(1)       NOT NULL,
    sort_order  NUMBER(11)      DEFAULT 0 NOT NULL,
    status      NUMBER(1)       DEFAULT 1 NOT NULL,
    icon_url    VARCHAR2(500),
    CONSTRAINT fk_category_parent FOREIGN KEY (parent_id) REFERENCES category(id) ON DELETE SET NULL
