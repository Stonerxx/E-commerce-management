-- 1. 用户表
CREATE TABLE "user" (
    id              NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username        VARCHAR2(50)    NOT NULL,
    password_hash   VARCHAR2(255)   NOT NULL,
    phone           VARCHAR2(20),
    email           VARCHAR2(100),
    avatar_url      VARCHAR2(500),
    status          NUMBER(1)       DEFAULT 1 NOT NULL,
    created_at      DATE            DEFAULT SYSDATE NOT NULL,
    last_login_at   DATE,
    CONSTRAINT uk_user_username UNIQUE (username),
    CONSTRAINT uk_user_phone UNIQUE (phone),
    CONSTRAINT uk_user_email UNIQUE (email)
);
COMMENT ON TABLE "user" IS '用户表';
COMMENT ON COLUMN "user".id IS '用户ID';
COMMENT ON COLUMN "user".username IS '用户名';
COMMENT ON COLUMN "user".password_hash IS '密码（加密存储）';
COMMENT ON COLUMN "user".phone IS '手机号';
COMMENT ON COLUMN "user".email IS '邮箱';
COMMENT ON COLUMN "user".avatar_url IS '头像URL';
COMMENT ON COLUMN "user".status IS '账号状态 0=禁用 1=正常';
COMMENT ON COLUMN "user".created_at IS '注册时间';
COMMENT ON COLUMN "user".last_login_at IS '最后登录时间';

-- 2. 角色表
